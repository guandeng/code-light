    /// 根据 config.language 更新当前语言
    static func update(preference: String) {
        switch preference {
        case "zh":     current = "zh"
        case "zh-Hant": current = "zh-Hant"
        case "en":     current = "en"
        default:       // auto → 跟随系统
            let lang = Locale.preferredLanguages.first ?? "zh"
            if lang.hasPrefix("zh") {
                // zh-Hant/zh-TW/zh-HK → 繁体，其余 zh → 简体
                current = (lang.contains("Hant") || lang.contains("TW") || lang.contains("HK")) ? "zh-Hant" : "zh"
            } else {
                current = "en"
            }
        }
    }

